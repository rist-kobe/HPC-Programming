program main
  implicit none

  integer, parameter :: NSIZE = 1024

  integer :: i, j
  real,allocatable,dimension(:)   :: A, B, C
  real,allocatable,dimension(:,:) :: D, E, F
 
  allocate( A(1:NSIZE) )
  allocate( B(1:NSIZE) )
  allocate( C(1:NSIZE) )

  allocate( D(1:NSIZE,1:NSIZE) )
  allocate( E(1:NSIZE,1:NSIZE) )
  allocate( F(1:NSIZE,1:NSIZE) )

  do i = 1, NSIZE
     A(i) = 1.0
     B(i) = 2.0
     C(i) = 3.0
  end do

  do j = 1, NSIZE
  do i = 1, NSIZE
     D(i,j) = 1.0
     E(i,j) = 2.0
     F(i,j) = 3.0
  end do
  end do

!* =======================================================================
!* parallel with no loop  
!* =======================================================================
!----------------------------------------------------------------------
!  !$acc parallel
!----------------------------------------------------------------------
!  A(1) = B(1) + C(1) ! meaningless code ...
!----------------------------------------------------------------------
!  !$acc end parallel
!----------------------------------------------------------------------

!* =======================================================================
!* kernels with two loops  
!* =======================================================================
!----------------------------------------------------------------------
  !$acc kernels
!----------------------------------------------------------------------
  do i = 1, NSIZE
     A(i) = B(i) + C(i)
  end do

  do j = 1, NSIZE
  do i = 1, NSIZE
     D(i,j) = E(i,j) * F(i,j)
  end do
  end do
!----------------------------------------------------------------------
  !$acc end kernels
!----------------------------------------------------------------------

!* =======================================================================
!* parallel with two loops  
!* =======================================================================
!----------------------------------------------------------------------
  !$acc parallel
!----------------------------------------------------------------------
  do i = 1, NSIZE
     A(i) = B(i) + C(i)
  end do

  do j = 1, NSIZE
  do i = 1, NSIZE
     D(i,j) = E(i,j) * F(i,j)
  end do
  end do
!----------------------------------------------------------------------
  !$acc end parallel
!----------------------------------------------------------------------

!* =======================================================================
!* parallel with two loops: Secifying loop directives  
!* =======================================================================
!----------------------------------------------------------------------
  !$acc parallel
!----------------------------------------------------------------------
  !$acc loop
  do i = 1, NSIZE
     A(i) = B(i) + C(i)
  end do

  !$acc loop
  do j = 1, NSIZE
  do i = 1, NSIZE
     D(i,j) = E(i,j) * F(i,j)
  end do
  end do
!----------------------------------------------------------------------
  !$acc end parallel
!----------------------------------------------------------------------

!* =======================================================================
!* parallel with two loops: Specifying loop directives with collapse 
!* =======================================================================
!----------------------------------------------------------------------
  !$acc parallel
!----------------------------------------------------------------------
  !$acc loop
  do i = 1, NSIZE
     A(i) = B(i) + C(i)
  end do

  !$acc loop collapse(2)
  do j = 1, NSIZE
  do i = 1, NSIZE
     D(i,j) = E(i,j) * F(i,j)
  end do
  end do
!----------------------------------------------------------------------
  !$acc end parallel
!----------------------------------------------------------------------

  stop
end program main
